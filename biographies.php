        <h3>Flóra Acosta</h3>
        <p>
          Flora started her musical studies at the age of 8 at the Institution of Elementary Music Education of Kispest
          (Budapest, Hungary). She was 10 years old when she has started her clarinet education under the guidance of
          Professor János Parák (first-clarinetist of the Budapest Operett Theater). Later, she continued her studies at the
          "Szent István" Conservatory of Music and High School with Professor Géza Bánhegyi (first-clarinetist of the
          Hungarian National Opera). After finishing her secondary education, she has started the Conservatory of Szeged
          with János Török (first-clarinetist of Szeged Symphony Orchestra) and József Németh (second-clarinetist of the
          Festival Orchestra of Budapest). She finished her degree at the Royal Conservatory of Music in Madrid with
          Professor Pedro Garbajosa.
        </p>
        <br>
        <p>
          Among her artistic activities we must mention her concerts at the Herrenberg House of Culture (Germany) at
          the age of 14, and her participation in the Bands Competitions of Poland, Czech Republic and Hungary. She
          helped making the artistic life of Szeged more alive. For example: concert for the Accommodation of Unfortunate
          Children at the Móra Ferenc Museum and by taking part in the opening ceremony of the Munkácsy Exhibition
          at the Reök Palace. During this time she also presented the works of local composer Imre Erik Nagy, who currently
          won numerous awards with his compositions. She also gave charity concerts for the local catholic churches and
          gave solo concerts a couple of times at the Szeged House of Culture.
        </p>
        <br>
        <p>

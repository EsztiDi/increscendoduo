        <h3>Daniel Acosta</h3>
        <p>
          He began his musical studies at the age of 6, at the "José Reyna" Music School in Caracas, Venezuela. In 1996 he
          started learning Classical Guitar with Professor Cesar Rico till 2011. In 2012 he entered the Experimental
          University of Arts in Caracas to study from the classical guitar Maestro Luis Zea for one year. After that he
          transferred to the Royal Conservatory of Madrid to complete his studies in classical guitar with the renowned
          teacher and guitarist Miguel Trápaga.
        </p>
        <br>
        <p>
          Among his recitals as a soloist the following are the concerts to homage well renowned composers: he performed
          for the Homage to "Leopoldo Igarza" at the "Prudencio Esaá" Music School and at the "Casa del Artista" in Caracas,
          played for the Homage to Antonio Lauro at the first guitar festival in Caracas, performed for the Homage to the
          Composer Francisco Rodrigo at José Ángel Lamas Conservatory in Caracas and played for the Homage to "Antonio
          Lauro" at the CELARG Foundation in Caracas. He was invited to the 1st Festival of Young Guitarists in Caracas
          in 2009 and in 2016 he also participated in the 3rd Festival of Young Guitarists “José Luis Rodrigo” in Madrid,
          Spain. On numerous occasions he performed at the Casa Castilla La Mancha and at the Casa de Cantabria in
          Madrid, as well as participating at the event "Suite Iberoamericana" at the Teatros del Canal in Madrid. Apart
          from these he took part in multiple guitar competitions where he received awards for his performance: 2011 – 6th
          International Guitar Festival in Caracas, Venezuela – Honorary Award. 2012 – 3rd International Guitar
          Competition in Lima, Peru – Honorary Award. 2015 – 1st Online Musical Competition “Gran Pausa” in Spain –
          Honorary Award. 2017 – Miguel Angel Colmenero Foundation in Madrid, Spain – Honorary Award. As for his
          orchestral experience, he played with the Band of the Royal Conservatory of Music of Madrid, and with the
          renowned Municipal Symphonic Band of Madrid under the direction of Rafael Sanz Espert.
        </p>
        <br>
        <p>
          He attended master classes with professors such as Leopoldo Igarza, José Luis Presa, Guillermo Flores and Richard
          Arellano from Venezuela, Eduardo Fernández from Uruguay, as well as with the Spanish members of the Guitar
          Quartet "Los Romero", Ángel, Pepe and Celín Romero. He participated in the “Harmony and Counterpoint” course
          by the composer Francisco Rodrigo in Caracas, and in Madrid he attended the course "Mastery, composition and
          musical aesthetics of the current guitar" taught by Leo Brouwer and Alex Garrobé. Lastly, he took part in the
          course for the appreciation of contemporary music by the composer José Baroni.
        </p>

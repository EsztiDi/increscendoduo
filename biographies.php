          Conservatory of Music in Madrid. For her involvement with concerts organized at the House of Castilla La
          Macha (Madrid) and House of Cantabria (Madrid), she received the Amana Prize of the Miguel Angel Colmenero
          Foundation. She participated in the celebration of Hungarian national holiday in the Hungarian Embassy of
          Madrid. She was the soloist for the Symphonic Band of Las Rozas (Madrid). She played with the Symphonic
          Orchestra of Madrid. Also she was the soloist of the Santa Cecília Orchestra of Madrid.
        </p>
